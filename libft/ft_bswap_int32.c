#include "libft.h"

int		ft_bswap_int32(int val)
{
	int tmp;

	tmp = ((val << 8) & 0xFF00FF00) | ((val >> 8) & 0xFF00FF);
	return (tmp << 16) | ((tmp >> 16) & 0xFFFF);
}
